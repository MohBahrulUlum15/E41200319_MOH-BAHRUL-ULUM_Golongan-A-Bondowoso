package minggu4;

public class latihan4 {

    //+++++ Statement -- for - each +++++//

    public static void main(String[] args) {

        //membuat array
        int angka[] = {3, 1, 42, 24, 12};

        //menggunakan perulangan for-each untuk menampilkan angka
        for (int x : angka) {
            System.out.print(x + " ");
        }
    }
}
